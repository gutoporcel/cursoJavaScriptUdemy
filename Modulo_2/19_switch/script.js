// switch

let nome ="matheus"

switch (nome) {
   case "matheus" :
      console.log("O nome é Matheus");
      break;
   case "jose":
      console.log("O nome é jose ");
      break;
   default:
      console.log("O nome nao foi encontrado");
      break;
}

